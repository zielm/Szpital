    private void zapiszWypisButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_zapiszWypisButtonActionPerformed
        Date dataWypisu;
        try {
            dataWypisu = dateFormat.parse(dataWypisuText.getText());
        } catch (ParseException ex) {
            Logger.getLogger(WizytaEdycjaJPanel.class.getName()).log(Level.SEVERE, null, ex);
            odrzucWypis();
            return;
        }
        
        if(dataWypisuText.getText().isEmpty() || typComboBox.getSelectedIndex() == 0) {
            odrzucWypis();
            return;
        }
        
        if(wypis == null) {
            wypis = new Wypis(wizyta.getIdWizyty());
        }
        
        wypis.setDataWypisu(dataWypisu);
        wypis.setTypWypisu(typComboBox.getSelectedItem().toString());
        wypis.setZalecenia(zaleceniaTextArea.getText());
        
        try {
            
            Main.getEntityManager().getTransaction().begin();
            Main.getEntityManager().persist(wypis);           

            Main.getEntityManager().getTransaction().commit();
            stanWypisLabel.setText("Pomyślnie edytowano wypis");
            stanWypisLabel.setForeground(Color.BLUE);
            zapiszButton.setEnabled(false);
            
        } catch (Exception SQLException) {
            odrzucWypis();
        }            
    }//GEN-LAST:event_zapiszWypisButtonActionPerformed

