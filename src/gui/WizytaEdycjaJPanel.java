    private void setDaneWizyty() {
        
        String idRozpoznania = "", idProcedury = "";
        if(wizyta.getIdRozpoznania()!= null) {
            idRozpoznania = wizyta.getIdRozpoznania().getIdRozpoznania();
        }
        
        if(wizyta.getIdProcedury() != null) {
            idProcedury = wizyta.getIdProcedury().getIdProcedury();
        }
                
        idText.setText(Long.toString(wizytaPacjenta.getIdPacjenta()));
        pacjentText.setText(wizytaPacjenta.getImie()+" "+wizytaPacjenta.getNazwisko());
        dataPrzyjeciaText.setText(dateFormat.format(wizytaPacjenta.getDataPrzyjecia()));
        lekarzText.setText(wizyta.getLekarzProwadzacy());
        opisTextArea.setText(wizyta.getOpisDolegliwosci());
        
        dodatkowyPanel.setDane(idRozpoznania, idProcedury);    
        
        wypis = wizyta.getWypis();
        if(wypis != null) {
            dataWypisuText.setText(dateFormat.format(wypis.getDataWypisu()));
            System.out.println(wypis.getTypWypisu());
            typComboBox.setSelectedItem(wypis.getTypWypisu());
            zaleceniaTextArea.setText(wypis.getZalecenia());
       }
    }

