    private void edytujButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_edytujButtonActionPerformed
        if (tabela.getSelectedRow() == -1 ) return;
        String id = tabela.getValueAt(tabela.getSelectedRow(), 0).toString();
        rozpoznanie = Main.getEntityManager().find(Rozpoznanie.class, id);
        
        edycjaPanel.setVisible(true);
        idText.setText(rozpoznanie.getIdRozpoznania());
        nazwaText.setText(rozpoznanie.getNazwaChoroby());
    }//GEN-LAST:event_edytujButtonActionPerformed

