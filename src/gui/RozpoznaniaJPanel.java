    private void usunButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_usunButtonActionPerformed
        if (tabela.getSelectedRow() == -1 ) return;
        String id = tabela.getValueAt(tabela.getSelectedRow(), 0).toString();
        rozpoznanie = Main.getEntityManager().find(Rozpoznanie.class, id);
        
        int odpowiedz = JOptionPane.showConfirmDialog(null,
            "Czy na pewno chcesz usunąć wybraną rozpoznanie?",
            "Usuń rozpoznanie",
            JOptionPane.YES_NO_OPTION);
        
        if(odpowiedz == JOptionPane.YES_OPTION) {
            Main.getEntityManager().getTransaction().begin();
            Main.getEntityManager().remove(rozpoznanie);
            Main.getEntityManager().getTransaction().commit();
            
            wykonanoStan.setText("Akcja wykonana pomyślnie");
            wykonanoStan.setForeground(Color.blue);
            setTabela();
        }
        
        
        
    }//GEN-LAST:event_usunButtonActionPerformed

